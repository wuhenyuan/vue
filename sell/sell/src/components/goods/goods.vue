<template>
  <div class="goods">
    <div class="menu-wrapper" ref="menuWrapper">
    	<ul>
    		<li v-for="(item,index) in goods" class="menu-item" :class="{'current': currentIndex == index}" @click="selectMenu(index,$event)">
    			<span class="text border-1px">
    				<span v-show="item.type>0" class="icon" :class="classMap[item.type]"></span>
    				{{item.name}}
    			</span>
    		</li>
    	</ul>
    </div>
    <div class="foods-wrapper" ref="foodWrapper">
    	<ul>
    		<li v-for="item in goods" class="foodlist food-list-hook">   "<!-- food-list-hook"钩子函数 -->
    			<h1 class="title">{{item.name}}</h1>
    			<ul>
    				<li @click="selectFood(food,$event)" v-for="food in item.foods" class="food-item border-1px">
    					<div class="icon">
    					   <img :src="food.icon">	
    					</div>
    					<div class="content">
                            <h2 class="name">{{food.name}}</h2>
                            <p class="description">{{food.description}}</p>
                            <div class="extra">
                            	<span class="count">月售{{food.sellCount}}</span>
                                <span>好评率：{{food.rating}}</span>
                            </div>
                            <div class="price">
                            	<span class="now">￥{{food.price}}</span>
                            	<span v-show="food.oldPrice" class="old">￥{{food.oldPrice}}</span>
                            </div>
                            <div  class="cartcontrol-wrapper">
                            	<cartcontrol v-on:cart-add="cartAdd" :food="food"></cartcontrol>
                            </div>
    					</div>
    				</li>
    			</ul>
    		</li>
    	</ul>
    </div>
    <shopcart ref="shopcart" :select-foods ="selectFoods" :delivery-price="seller.deliveryPrice" :min-price="seller.minPrice"></shopcart>
       <food :food="selectedFood" ref="food"></food>
  </div>
</template>

<script type="text/ecmascript-6">
 import BScroll from 'better-scroll'
 import shopcart from '../shopcart/shopcart.vue' 
 import cartcontrol from "../cartcontrol/cartcontrol.vue"
 import food from "../food/food.vue"
 import Vue from "vue"
const ERR_OK = 0;
const eventHub = new Vue()
 export default{
 	props:{
 		seller:{
 			type: Object
 		}
 	},
 	data() {
       return{
       	 goods:[],
       	 listHeight: [],
       	 scrollY: 0,
         selectedFood:{}
       	};
 	},
 	computed : {
 		currentIndex() {
 			for(let i=0;i < this.listHeight.length;i++){
 				let height1 = this.listHeight[i];
 				let height2 = this.listHeight[i+1];
 				if( !height2 || (this.scrollY >= height1 && this.scrollY < height2)){
 					return i;
 				} 				
 			}
 			return 0;
 		},
 		selectFoods(){
 			let foods = [];
 			this.goods.forEach((good)=>{
 				good.foods.forEach((food)=>{
 					if(food.count){
 						foods.push(food);
 					}
 				});
 			});
 			return foods;
 		}
 	},
 	created() {
 		this.classMap = ['decrease','discount','special','invoice','guarantee'];

 		this.$http.get('/api/goods').then((response) => {
 			response =response.body;
 			if(response.errno === ERR_OK){
 				this.goods = response.data;
 				this.$nextTick(()=>{
 					this._initScroll();  //初始化scroll
          this._calculateHeight();  //初始化列表高度
 				});
 			}
 		});
 	},
 	methods: {
     		selectMenu(index,event) {
              if(!event._constructed){
              	return ;
              }
              let foodList = this.$refs.foodWrapper.getElementsByClassName('food-list-hook');
              let el = foodList[index];
              this.foodsScroll.scrollToElement(el,300);//300毫秒的动画时间
     		},
        selectFood(food,event) {
           if(!event._constructed){
                return ;
              }
              this.selectedFood = food;
              this.$refs.food.show();
        },
        cartAdd(target) {
           //拿到traget(DOM对象)之后，将其传入shopcart组件中drop(target){}方法，
           //此处用this.$refs调用子组件,访问DOM时用的是ref="menuWrapper"
            this.$nextTick(() => { //回调函数异步执行，两个动画效果就不会卡顿了
              this.$refs.shopcart.drop(target);
            });
        },
        _initScroll() {
        	this.menuScroll = new BScroll(this.$refs.menuWrapper, {
        		click:true
        	});

        	this.foodsScroll = new BScroll(this.$refs.foodWrapper, {
        		probeType: 3, //希望scroll在滚动的时候，能实时告诉我们滚动的位置
        		click:true
        	});

        	this.foodsScroll.on('scroll',(pos) => {
        		this.scrollY = Math.abs(Math.round(pos.y)); 

        	})
        },
        _calculateHeight() {
        	let foodList = this.$refs.foodWrapper.getElementsByClassName('food-list-hook');
        	let height = 0;
        	console.log(foodList);
        	this.listHeight.push(height);
        	for(let i=0;i<foodList.length;i++){
        		let item = foodList[i];
        		height += item.clientHeight;
        		this.listHeight.push(height);
        	}
        	//console.log(this.listHeight);
        },
 	},  
 	components: {
 		shopcart,
 		cartcontrol,
    food
 	},
  event:{
    'car-add'(target) {
      this._drop(target);
    }
  }
 };
</script>

